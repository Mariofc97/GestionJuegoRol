
public abstract class Raza {

	private int fuerza;
	private int magia;
	private int suerte;
	
	//Constructor
	
	public Raza(int fuerza, int magia, int suerte) {
		this.fuerza=fuerza;
		this.magia=magia;
		this.suerte=suerte;
}
	
	//Getters Setters


	public int getFuerza() {
		return fuerza;
	}

	public void setFuerza(int fuerza) {
		this.fuerza = fuerza;
	}

	public int getMagia() {
		return magia;
	}

	public void setMagia(int magia) {
		this.magia = magia;
	}

	public int getSuerte() {
		return suerte;
	}

	public void setSuerte(int suerte) {
		this.suerte = suerte;
	}



	
	
	
	
	
	
}
